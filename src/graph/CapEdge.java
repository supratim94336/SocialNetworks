public class CapEdge {
    //------------------------------------------------------------------------------------------------------------------
    // Variables--------------------------------------------------------------------------------------------------------
    //------------------------------------------------------------------------------------------------------------------
    public CapNode fromID;
    public CapNode toID;

    //------------------------------------------------------------------------------------------------------------------
    // Constructor------------------------------------------------------------------------------------------------------
    //------------------------------------------------------------------------------------------------------------------
    public CapEdge(CapNode fromID, CapNode toID) {
        this.fromID = fromID;
        this.toID = toID;
    }
    //------------------------------------------------------------------------------------------------------------------
    // Functions--------------------------------------------------------------------------------------------------------
    //------------------------------------------------------------------------------------------------------------------

    // Utility Functions
    public CapNode getOtherEnd(CapNode cp) {
        if(cp.equals(fromID)) {
            return toID;
        } else {
            return fromID;
        }
    }
    public CapNode getFromID() {
        return this.fromID;
    }
    public CapNode getToID() {
        return this.toID;
    }
}
